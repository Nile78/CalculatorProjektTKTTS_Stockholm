public class CalculationBackEndOperators {

    public double operatorPlus(double firstNumber, double secondNumber) {
        return firstNumber + secondNumber;
    }

    public double operatorMinus(double firstNumber, double secondNumber) {
        return firstNumber - secondNumber;

    }

    public double operatorDivision(double firstNumber, double secondNumber) {
        return firstNumber / secondNumber;
    }

    public double operatorMultiplication(double firstNumber, double secondNumber) {
        return firstNumber * secondNumber;

    }

}












